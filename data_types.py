# data types and variables

x = 33
print(type(x))

# converting the data type using type casting

y = float(x)
print(x,type(y))

# converting to string

z = str(x)
print(z , type(z))

# string to list

list1 = list(z)
print(list1,type(list1))

# converting list to tuple using type casting

tuple1 = tuple(list1)
print(tuple1,type(tuple1))
